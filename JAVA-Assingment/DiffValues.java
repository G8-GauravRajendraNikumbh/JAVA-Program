class DiffValues
{
	public static void main(String args[])
	{
		int fno=0,sno=0;
		float x=3.14F,y=10.0f;
		char ch='c';
		System.out.println("Value of integer variable of FNO="+fno);
		System.out.println("Value of integer variable of SNO="+sno);
		System.out.println("Value of Float variable of X="+x);
		System.out.println("Value of Float variable of Y="+y);
		System.out.println("Vlaue of Charater variable of ch="+ch);
	}
}
